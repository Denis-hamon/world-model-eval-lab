#!/usr/bin/env python3
"""Generate the multi-model CPG table from the committed result JSONs.

Scans `results/**/*.json` for counterfactual-planning-gap reports (single-arm
and multi-arm shapes; smoke runs and sweep matrices are excluded), keeps the
highest-n row per (environment, model, planner, init) cell, and writes
`results/MODEL_TABLE.md`.

Stdlib-only, like everything in `src/wmel/`. Run from the repo root:

    python scripts/build_model_table.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO_ROOT / "src"))

from wmel.report import (  # noqa: E402
    dedupe_model_table_rows,
    model_table_rows_from_report,
    to_markdown_model_table,
)

RESULTS_DIR = REPO_ROOT / "results"
OUTPUT_PATH = RESULTS_DIR / "MODEL_TABLE.md"

HEADER = """\
# Multi-model CPG table

Generated by `scripts/build_model_table.py` from the JSONs under `results/`.
Do not edit by hand; re-run the script after landing new result files.

Each row is the highest-n run committed for that (environment, model,
planner, init) cell -- pooled multi-seed results supersede per-seed runs.
`Init` distinguishes fixed-initial-state runs from task-level runs that
sample the initial-state distribution (`--varied-init`); the two are not
comparable and are reported as separate rows. CPG > 0 means the oracle
planner outperforms the same planner on the learned dynamics.
"""


def collect_rows() -> list:
    rows = []
    for path in sorted(RESULTS_DIR.rglob("*.json")):
        try:
            report = json.loads(path.read_text())
        except (json.JSONDecodeError, UnicodeDecodeError):
            continue
        if not isinstance(report, dict):
            continue
        rows.extend(
            model_table_rows_from_report(
                report, source=str(path.relative_to(REPO_ROOT))
            )
        )
    return rows


def main() -> None:
    rows = collect_rows()
    deduped = dedupe_model_table_rows(rows)
    table = to_markdown_model_table(deduped)
    sources = "\n".join(
        f"- `{r.environment}` / `{r.model}` / `{r.planner}` / {r.init}: `{r.source}`"
        for r in deduped
    )
    OUTPUT_PATH.write_text(
        HEADER + "\n" + table + "\n## Row sources\n\n" + sources + "\n"
    )
    print(f"{len(rows)} rows scanned, {len(deduped)} kept")
    print(f"Wrote {OUTPUT_PATH.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
